$("#li-toggle_1").click( function () {
            if (document.getElementById('li-today-1')) {

                if (document.getElementById('li-today-1').style.display == 'none') {
                    document.getElementById('li-today-1').style.display = 'block';
                    document.getElementById('li-today-2').style.display = 'none';
                    document.getElementById('li-today-3').style.display = 'none';
                    document.getElementById('li-today-4').style.display = 'none';
                    document.getElementById('li-today-5').style.display = 'none';
                    document.getElementById('li-today-6').style.display = 'none';
                    document.getElementById('li-today-7').style.display = 'none';
                }
                else {
                    document.getElementById('li-today-1').style.display = 'none';
                }
            }
}
);
